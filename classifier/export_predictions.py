"""Export classifier predictions for all entries as a JS bundle for the webapp.

Runs bi-encoder + optional reranker on all 41 entries, marks which candidates
are hand-curated vs. model-suggested, and writes docs/classifier-predictions.js.

Usage:
    python -m classifier.export_predictions
    python -m classifier.export_predictions --rerank --top-k 20
"""

import argparse
import json
import sys
import time

import numpy as np
from sentence_transformers import SentenceTransformer

from .config import BIENCODER_MODEL, INDEX_DIR, DEFAULT_TOP_K
from .data_loader import load_entries, build_query


def load_index():
    import faiss
    index_path = INDEX_DIR / "controls.index"
    meta_path = INDEX_DIR / "controls_meta.json"
    if not index_path.exists():
        print("ERROR: Index not found. Run 'python -m classifier.index_builder' first.")
        sys.exit(1)
    index = faiss.read_index(str(index_path))
    meta = json.loads(meta_path.read_text("utf-8"))
    return index, meta


def export(top_k: int = DEFAULT_TOP_K, use_reranker: bool = False) -> None:
    from .config import CLASSIFIER_DIR

    print(f"Exporting predictions (top-{top_k}, rerank={use_reranker})")

    index, meta = load_index()
    model = SentenceTransformer(BIENCODER_MODEL)
    entries = load_entries()

    reranker_fn = None
    if use_reranker:
        from .reranker import rerank
        reranker_fn = rerank

    all_predictions = []
    t0 = time.time()

    for i, entry in enumerate(entries):
        query = build_query(entry["id"], entry["name"], entry["severity"])
        q_emb = model.encode([query], normalize_embeddings=True)
        q_emb = np.array(q_emb, dtype=np.float32)

        search_k = 50 if use_reranker else top_k
        search_k = min(search_k, index.ntotal)
        scores, indices = index.search(q_emb, search_k)

        candidates = []
        for score, idx in zip(scores[0], indices[0]):
            if idx < 0:
                continue
            m = meta[idx]
            candidates.append({
                "framework": m["framework"],
                "control_id": m["control_id"],
                "title": m["title"],
                "function": m.get("function"),
                "score": float(score),
                "text": f"{m['framework']} -- {m['control_id']}: {m['title']}",
            })

        if reranker_fn:
            candidates = reranker_fn(query, candidates, top_k=top_k)
        else:
            candidates = candidates[:top_k]

        # Mark curated
        curated_set = {
            (m["framework"], m["control_id"])
            for m in entry.get("mappings", [])
        }

        for rank, c in enumerate(candidates):
            key = (c["framework"], c["control_id"])
            all_predictions.append({
                "entry_id": entry["id"],
                "entry_name": entry["name"],
                "source_list": entry["source_list"],
                "framework": c["framework"],
                "control_id": c["control_id"],
                "title": c["title"],
                "score": round(c.get("score", 0), 4),
                "rank": rank + 1,
                "curated": key in curated_set,
            })

    elapsed = time.time() - t0

    # Normalize scores to 0-1 for confidence visualization
    scores = [p["score"] for p in all_predictions]
    if scores:
        s_min, s_max = min(scores), max(scores)
        s_range = s_max - s_min if s_max != s_min else 1.0
        for p in all_predictions:
            p["confidence"] = round((p["score"] - s_min) / s_range, 4)

    # Write JS bundle
    output_path = CLASSIFIER_DIR.parent / "docs" / "classifier-predictions.js"
    js = [
        "// Auto-generated by classifier/export_predictions.py",
        f"// Generated: {time.strftime('%Y-%m-%d')}",
        f"// Entries: {len(entries)}, Predictions: {len(all_predictions)}",
        f"// Mode: {'reranker' if use_reranker else 'biencoder'}, top-k: {top_k}",
        f"window.CROSSWALK_PREDICTIONS = {json.dumps(all_predictions, indent=2)};",
    ]
    output_path.write_text("\n".join(js), encoding="utf-8")

    # Stats
    curated_count = sum(1 for p in all_predictions if p["curated"])
    suggested_count = len(all_predictions) - curated_count
    print(f"  {len(all_predictions)} predictions for {len(entries)} entries in {elapsed:.1f}s")
    print(f"  {curated_count} curated, {suggested_count} model-suggested")
    print(f"  Written: {output_path}")


def main():
    parser = argparse.ArgumentParser(description="Export classifier predictions for webapp")
    parser.add_argument("--top-k", type=int, default=15, help="Candidates per entry")
    parser.add_argument("--rerank", action="store_true", help="Use cross-encoder reranker")
    args = parser.parse_args()
    export(args.top_k, args.rerank)


if __name__ == "__main__":
    main()
